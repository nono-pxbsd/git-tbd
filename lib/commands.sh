#!/bin/bash

# Commande pour d√©marrer une nouvelle branche
# Utilise fzf pour s√©lectionner le type de branche et demande le nom
# Si un argument est pass√©, il est utilis√© pour d√©terminer le type et le nom
# Si aucun argument n'est pass√©, demande interactive avec fzf
start() {
  local input="$1"
  local branch_type=""
  local name=""

  if [[ -n "$input" && "$input" == */* ]]; then
    branch_type="${input%%/*}"
    name="${input#*/}"
  fi

  # Si aucun type fourni, demande interactive avec fzf
  if [[ -z "$branch_type" || -z "$name" ]]; then
    if ! command -v fzf >/dev/null; then
      echo -e "${YELLOW}‚ö†Ô∏è  La commande 'fzf' est requise si aucun argument n'est pass√©.${RESET}"
      return 1
    fi
    branch_type=$(printf "%s\n" "${BRANCH_TYPES[@]}" | fzf --prompt="üß≠ Type de branche ? > " --height=10%)
    [[ -z "$branch_type" ]] && { echo -e "${YELLOW}‚ö†Ô∏è  Aucun type s√©lectionn√©.${RESET}"; return 1; }
    echo -ne "üìù Nom de la branche : "
    read name
    [[ -z "$name" ]] && { echo -e "${YELLOW}‚ö†Ô∏è  Nom requis.${RESET}"; return 1; }
  fi

  create_branch "$branch_type" "$name"
}

# Commande pour terminer une branche
# Fusionne la branche dans main et la supprime
# Si --pr est pass√©, ouvre une PR sur GitHub
# Si aucun argument n'est pass√©, d√©duit le type et le nom depuis la branche courante
# Si un argument est pass√©, il peut √™tre de la forme type/name ou juste type
# Si deux arguments sont pass√©s, ils sont consid√©r√©s comme type et nom
# Si la branche courante est de type feature, fix, hotfix ou chore, elle est utilis√©e pour d√©duire le type et le nom
# Si la branche courante n'est pas de type support√©, affiche un message d'erreur
# Si la branche courante est de type support√©, fusionne et supprime la branche
# Si --pr est pass√©, ouvre une PR sur GitHub apr√®s avoir publi√© la branche
# Si la branche courante n'existe pas, affiche un message d'erreur
# Si la branche courante existe mais n'est pas publi√©e, publie la branche avant de fusionner
# Si la branche courante est d√©j√† fusionn√©e, affiche un message d'information
# Si la branche courante est fusionn√©e avec succ√®s, affiche un message de succ√®s
# Si la branche courante est fusionn√©e mais ne peut pas √™tre supprim√©e, affiche un message d'avertissement
# Si la branche courante est fusionn√©e mais ne peut pas √™tre supprim√©e √† distance, affiche un message d'avertissement
# Si la branche courante est fusionn√©e et supprim√©e avec succ√®s, affiche un message de succ√®s
finish() {
  local type=""
  local name=""
  local branch=""
  local current=""
  local open_pr=false

  # R√©cup√©ration du HEAD
  current=$(git rev-parse --abbrev-ref HEAD)

  # V√©rification pr√©sence de --pr
  for arg in "$@"; do
    if [[ "$arg" == "--pr" ]]; then
      open_pr=true
      set -- "${@/--pr/}" # suppression de l'argument de la liste
      break
    fi
  done

  # D√©duction des arguments restants
  if [[ $# -eq 0 ]]; then
    # D√©duire depuis la branche courante
    if [[ "$current" == */* ]]; then
      type="${current%%/*}"
      name="${current##*/}"
    else
      echo -e "${YELLOW}‚ö†Ô∏è Impossible de d√©terminer type/nom depuis la branche actuelle ($current).${RESET}"
      return 1
    fi
  elif [[ $# -eq 1 ]]; then
    if [[ "$1" == */* ]]; then
      type="${1%%/*}"
      name="${1##*/}"
    else
      type="$1"
      name="${current##*/}"
    fi
  elif [[ $# -eq 2 ]]; then
    type="$1"
    name="$2"
  else
    echo -e "${YELLOW}‚ö†Ô∏è Trop d'arguments. Usage : finish [type[/name]] | type name [--pr]${RESET}"
    return 1
  fi

  # Validation du type
  if [[ ! "$type" =~ ^(feature|fix|hotfix|chore)$ ]]; then
    echo -e "${YELLOW}‚ö†Ô∏è Type non support√© : ${type}.${RESET}"
    return 1
  fi

  branch="${type}/${name}"
  label="${type}(${name})"

  if [[ "$open_pr" == true ]]; then
    validate_pr "$branch" || {
      echo -e "${RED}‚ùå √âchec de validation de la branche ${branch}, PR annul√©e.${RESET}"
      return 1
    }
    open_pr "$branch"
    return 0
  fi

  # Merge et suppression
  echo -e "${GREEN}üîÄ Fusion de la branche ${branch} dans main...${RESET}"
  git checkout main && git pull || return 1
  git merge --no-ff "$branch" -m "$label: merge ${type} into main" || return 1

  if git show-ref --verify --quiet "refs/heads/${branch}"; then
    git branch -d "$branch"
  fi
  
  delete_remote_branch "$branch"

  echo -e "${GREEN}‚úÖ Branche ${branch} fusionn√©e et supprim√©e.${RESET}"
}

# Commande pour publier une branche
# Publie la branche courante vers l'origin
# Si un argument est pass√©, il est utilis√© comme nom de branche
# Si aucun argument n'est pass√©, utilise la branche courante
# Si la branche n'existe pas, affiche un message d'erreur
# Si la branche est propre, publie la branche
# Si la branche n'est pas propre, affiche un message d'erreur
# Si la branche est d√©j√† synchronis√©e avec l'origin, affiche un message d'information
# Si la branche n'est pas synchronis√©e, synchronise la branche avec l'origin
# Si la synchronisation √©choue, affiche un message d'erreur
# Si la branche est publi√©e avec succ√®s, affiche un message de succ√®s
# Si la branche n'est pas publi√©e, affiche un message d'erreur
publish() {
  local force=false
  local branch="${1:-$(git symbolic-ref --short HEAD)}"

  # G√®re les arguments comme --force
  for arg in "$@"; do
    case "$arg" in
      --force-sync) force_sync=true ;;
      --force-push) force_push=true ;;
    esac
  done

  if ! local_branch_exists "$branch"; then
    echo -e "${RED}‚úò La branche locale '$branch' n'existe pas.${RESET}"
    return 1
  fi

  if is_branch_clean "$branch"; then
    echo "‚úÖ La branche "$branch" est propre."
  else
    echo "‚ùå La branche "$branch" n‚Äôest pas propre ou impossible √† v√©rifier."
    exit 1
  fi

  if ! branch_is_sync "$branch"; then
    local status
    status=$(get_branch_sync_status "$branch")

    if [[ "$status" == "behind" && "$force_sync" == true ]]; then
      sync_branch_to_remote --force "$branch" || return 1
    else
      sync_branch_to_remote "$branch" || return 1
    fi
  fi

  echo -e "${BLUE}üöÄ Publication de la branche '$branch' vers origin...${RESET}"
  if [ "$force_push" == true ]; then
    git push -u origin "$branch" --force-with-lease || return 1
  else
    git push -u origin "$branch" || return 1
  fi

  echo -e "${GREEN}‚úÖ Branche publi√©e avec succ√®s.${RESET}"
}

# Commande pour ouvrir une Pull Request sur Github
# Ouvre une PR depuis la branche courante vers main
# Si un argument est pass√©, il est utilis√© comme nom de branche
# Si aucun argument n'est pass√©, utilise la branche courante
# Si la branche n'est pas une branche de travail valide, affiche un message d'avertissement
# Si la branche est valide, publie la branche et ouvre une PR
# Si la PR est cr√©√©e avec succ√®s, affiche un message de succ√®s avec le lien vers la PR
# Si la PR √©choue, affiche un message d'erreur
# Utilise gh pr create pour cr√©er la PR
# Utilise gh pr view pour obtenir l'URL de la PR cr√©√©e
# Si gh pr create √©choue, affiche un message d'erreur
# Si gh pr view √©choue, affiche un message d'erreur
# Si la PR est cr√©√©e, affiche un message de succ√®s avec le lien vers la PR
open_pr() {
  local input="$1"
  local branch=""
  local type=""
  local name=""

  if [[ -n "$input" ]]; then
    parse_branch_input "$input"
    type="$PARSED_TYPE"
    name="$PARSED_NAME"
    branch="$type/$name"
  else
    branch="$(git branch --show-current)"
    type="${branch%%/*}"
    name="${branch#*/}"
  fi

  if ! is_valid_work_branch "$branch"; then
    echo -e "${YELLOW}‚ö†Ô∏è  La branche '$branch' n'est pas une branche de travail valide.${RESET}"
    return 1
  fi

  local prefix="${BRANCH_ICONS[$type]}"
  local title="${prefix}${name}"
  local body="${2:-Pull request automatique depuis \`$branch\` vers \`main\`}"

  publish "$branch" || return 1

  echo -e "üîÅ Cr√©ation de la PR via GitHub CLI..."
  gh pr create --base main --head "$branch" --title "$title" --body "$body"

  local url
  url=$(gh pr view "$branch" --json url -q ".url")

  echo -e "${GREEN}‚úÖ PR cr√©√©e depuis ${CYAN}$branch${GREEN} vers main.${RESET}"
  echo -e "üîó Lien : ${BOLD}${url}${RESET}"
}


# Commande pour valider une Pull Request
# Valide une PR en v√©rifiant la propret√© de la branche, son existence et sa synchronisation
# Si un argument est pass√©, il est utilis√© comme nom de branche
# Si aucun argument n'est pass√©, utilise la branche courante
# Si la branche n'existe pas, affiche un message d'erreur
# Si la branche n'est pas propre, affiche un message d'erreur
# Si la PR n'existe pas, affiche un message d'erreur
# Si la branche n'est pas synchronis√©e avec l'origin, affiche un message d'avertissement
# Si la branche est synchronis√©e, affiche un r√©sum√© de la PR
# Si l'utilisateur confirme, valide la PR avec le mode de fusion sp√©cifi√© (local-squash, squash, merge)
# Si l'utilisateur refuse, annule la validation
validate_pr() {
  local branch=""
  local merge_mode="merge"
  local assume_yes=false
  local force_sync=false

  local current_branch
  current_branch=$(git rev-parse --abbrev-ref HEAD)

  # -- Parsing des arguments
  local args=()
  for arg in "$@"; do
    case "$arg" in
      --merge-mode=*) merge_mode="${arg#*=}" ;;
      -y|--assume-yes) assume_yes=true ;;
      --force-sync) force_sync=true ;;
      -*) ;;  # ignore les options inconnues
      *) args+=("$arg") ;;  # arguments restants (ex. : nom de branche)
    esac
  done

  # D√©tection de la branche cible
  if [[ ${#args[@]} -gt 0 ]]; then
    branch="${args[0]}"
  else
    branch="$current_branch"
  fi

  echo "üîç Validation de la PR sur branche : $branch"

  # V√©rifie existence de branche
  if ! local_branch_exists "$branch"; then
    echo -e "${RED}‚ùå La branche '$branch' n'existe pas.${RESET}"
    return 1
  fi

  # V√©rifie propret√© de la branche
  if ! is_branch_clean "$branch"; then
    echo -e "${RED}‚ùå La branche '$branch' n‚Äôest pas propre.${RESET}"
    return 1
  fi

  # V√©rifie si une PR existe
  echo "üîÑ V√©rification de l'existence d'une PR..."
  if ! gh pr view "$branch" &>/dev/null; then
    echo "‚ùå Aucune Pull Request trouv√©e pour la branche '$branch'."
    echo "üí° Cr√©ez une PR avec : git-tbd open_pr $branch"
    return 1
  fi

  # V√©rifie que la branche locale est bien synchronis√©e avec l'origin
  echo "üîÑ V√©rification de la synchronisation avec la remote..."
  git fetch origin "$branch" &>/dev/null

  # -- Synchronisation √©ventuelle
  local status
  status=$(get_branch_sync_status "$branch")

  if [[ "$status" != "synced" ]]; then
    echo "‚ö†Ô∏è  Branche '$branch' non synchronis√©e (statut: $status)."
    if [[ "$force" == true ]]; then
      echo "üîß Tentative de synchronisation forc√©e..."
      sync_branch_to_remote --force "$branch" || return 1
    else
      echo "üí° Corrigez cela avec : git-tbd publish $branch"
      return 1
    fi
  fi

  # -- Affichage du r√©sum√© de PR
  echo ""
  echo "üìã R√©sum√© de la PR :"
  gh pr view "$branch" --web

  # -- Confirmation ou mode automatique
  if ! $assume_yes; then
    read -r -p "‚úÖ Souhaitez-vous valider (merger) la PR ? (d√©tection du mode ensuite) [y/N] " confirm
  else
    confirm="y"
  fi

  if [[ ! "$confirm" =~ ^[Yy]$ ]]; then
    echo "‚ùå Validation annul√©e."
    return 1
  fi

  local commit_count
  commit_count=$(get_commit_count_between_branches "origin/main" "$current_branch")

  if [[ "$merge_mode" == "squash" && "$commit_count" -gt 1 ]]; then
    echo "üîÑ Plusieurs commits d√©tect√©s ($commit_count)."
    echo "‚ò£Ô∏è L'utilisation du mode 'squash' de Github entraina une synchronisation manuelle forc√©e."
    echo "üí° Nous vous conseillons l'utilisation du local-squash pour 1 seul commit ou un mode merge classique avec l'ensemble des commits."
    echo ""
    if ! $assume_yes; then
      read -r -p "‚úÖ Souhaitez-vous poursuivre avec un squash Github ? [y/N] " confirm_merge_mode
      if [[ "$confirm_merge_mode" =~ ^[Nn]$ ]]; then
        echo "‚ùå Squash Github annul√©."
        return 1
      else
        echo "üîÑ Choix du mode de merge : [ local-squash (rebase) | merge ]"
        read -r -p "‚úÖ Quelle m√©thode souhaitez vous utiliser ? (laisser vide pour local-squash) : " merge_mode
        if [[ "$merge_mode" == "merge" ]]; then
          echo "üëâ Choix manuel de la m√©thode merge"
        else
          echo "‚ÑπÔ∏è Utilisation de local-squash par d√©faut"
          merge_mode="local-squash"
        fi
      fi 
    else
      echo "üîÑ Choix automatique de la m√©thode local-squash"
      merge_mode="local-squash"
    fi
  fi

  if [[ "$merge_mode" == "local-squash" ]]; then
    echo "üîÑ Squash local en cours..."
    squash_commits_to_one "--method=rebase" || return 1
    echo "‚úÖ Squash local effectu√©."
    echo "üîÑ Publication de la branche apr√®s squash..."
    publish "$branch" --force-push || return 1
    echo "‚úÖ Publication de la branche apr√®s squash r√©ussie."
    $merge_mode="merge"
  fi

  echo "üöÄ Validation en cours avec --$merge_mode..."
  gh pr merge "$branch" --"$merge_mode" --delete-branch
  echo "üéâ PR valid√©e et branche supprim√©e."
}



